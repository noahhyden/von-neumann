// check-typography.mjs - enforce the repo typography rules (CLAUDE.md 5) across
// the papers module: no em-dash (U+2014), no en-dash (U+2013), no emoji.
//
// Math and astronomy symbols (Greek letters, the approx and much-greater signs,
// superscript two, the sun symbol, arrows) are NOT emoji and are allowed
// (CLAUDE.md 5). The emoji test targets the emoji code planes, the regional-
// indicator flag range, and the emoji variation selector, so it never flags the
// BMP symbol blocks where those allowed glyphs live.
//
// Scans authored sources only: *.tex, *.json, *.md, *.mjs. Skips generated and
// build artifacts (refs.bib, node_modules/, build/). Exits nonzero on any
// violation so CI fails the PR. Paths resolve relative to this file, so cwd does
// not matter.
//
// ASCII only, no em-dash (CLAUDE.md 5).

import { readdirSync, readFileSync, statSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { join, extname } from "node:path";

const root = fileURLToPath(new URL("..", import.meta.url)); // the papers/ module root

const CHECK_EXT = new Set([".tex", ".json", ".md", ".mjs"]);
const SKIP_DIR = new Set(["node_modules", "build"]);
const SKIP_FILE = new Set(["refs.bib"]); // generated by gen-bib.mjs; not authored here

// Each rule is [regex, human-readable reason]. The regexes match a single
// offending character so search() can report its column.
// Regexes are built from codepoints (not literal glyphs) so this file itself
// stays clean under its own scan.
const RULES = [
  [new RegExp("\\u2014"), "em-dash (U+2014); use an ASCII hyphen '-'"],
  [new RegExp("\\u2013"), "en-dash (U+2013); use an ASCII hyphen '-' (ranges too, e.g. 90-96%)"],
  // Emoji planes + regional-indicator flags + emoji variation selector + ZWJ.
  // Intentionally excludes U+2600-26FF and U+2700-27BF so astronomy/math symbols
  // and arrows are not false-positives.
  [/[\u{1F000}-\u{1FAFF}\u{1F1E6}-\u{1F1FF}\u{FE0F}\u{200D}]/u, "emoji; not allowed (CLAUDE.md 5)"],
];

/** Recursively list authored files under dir. */
function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    if (SKIP_DIR.has(name)) continue;
    const p = join(dir, name);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (CHECK_EXT.has(extname(name)) && !SKIP_FILE.has(name)) out.push(p);
  }
  return out;
}

const violations = [];
for (const file of walk(root)) {
  const rel = file.slice(root.length);
  const lines = readFileSync(file, "utf8").split("\n");
  lines.forEach((line, i) => {
    for (const [re, reason] of RULES) {
      const col = line.search(re);
      if (col >= 0) {
        violations.push(`${rel}:${i + 1}:${col + 1}: ${reason}\n    ${line.trim().slice(0, 120)}`);
      }
    }
  });
}

if (violations.length) {
  console.error(`check-typography: ${violations.length} violation(s):\n`);
  console.error(violations.join("\n"));
  process.exit(1);
}
console.log("check-typography: clean (no em-dash, en-dash, or emoji in authored files).");
